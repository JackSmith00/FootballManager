public class Formation {

	// Attributes
	private int defenders;
	private int midfielders;
	private int strikers;
	
	// Methods
	// Getters
	public int getDefenders() {
		return defenders;
	}
	public int getMidfielders() {
		return midfielders;
	}
	public int getStrikers() {
		return strikers;
	}
}
